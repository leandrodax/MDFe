    public Integer VerificaCRT(Object item){
        Integer crt = null;
        if(item != null){
            if(item.equals("1")){
                crt = 1;
            }else if(item.equals("2")){
                crt = 2;
            }else if(item.equals("3")){
                crt = 3;
            }
        }
        return crt;
    }
 

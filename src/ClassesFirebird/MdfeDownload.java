package ClassesFirebird;

import java.io.Serializable;


public class MdfeDownload implements Serializable {

    
    private String numero;
    
    private String cnpj;
    
    private String cpf;

    public MdfeDownload() {
    }

    public MdfeDownload(String numero) {
        this.numero = numero;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    
}

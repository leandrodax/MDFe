package ClassesFirebird;


public class MdfeCondutores{

    private String numero;
    
    private String nome;
    
    private String cpf;

    public MdfeCondutores() {
    }

    public MdfeCondutores(String numero) {
        this.numero = numero;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    
}
